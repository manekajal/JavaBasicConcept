package org.bridgelabz;

 public class PrimitiveDataType {
     static boolean value1;
     static double value2;
     static int value3;
     static float value4;
     static long value5;
     public static void main(String[] args)
     {
      System.out.println("Print Default value");
      System.out.println("Boolean = " + value1);
      System.out.println("Double = " + value2);
      System.out.println("Integer = " + value3);
      System.out.println("Float = " + value4);
      System.out.println("Long = " + value5);



     }
}
